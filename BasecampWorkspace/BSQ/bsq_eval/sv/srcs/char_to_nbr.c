void	char_to_nbr(char **grid, int x, int y, char *c)
{
	int i;
	int j;
	i = 0;
	j = 0;
	while(j < y)
	{
		while(i < x)
		{
			if(grid[i][j] == c[2])
				grid[i][j] = '1';
			else
				grid[i][j] = '0';
			i++;
		}
		i = 0;
		j++;
	}
}
